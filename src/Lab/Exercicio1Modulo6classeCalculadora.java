package Lab;

public class Exercicio1Modulo6classeCalculadora {

	// SUBTRAIR
	public double subtrair(double...list) {
		double soma = 0;
		for(double d:list) {
			soma -= d;
		}
		return soma;
	}

	public int subtrair(int...list) {
		int soma = 0;
		for(int d:list) {
			soma -= d;
		}
		return soma;
	}

	// MULTIPLICAR
	public int multiplicar(int...list) {
		int soma = 0;
		for(int d:list) {
			soma += d;
		}
		return soma;
	}

	public double multiplicar(double...list) {
		double soma = 0;
		for(double d:list) {
			soma += d;
		}
		return soma;
	}

	// DIVIDIR
	public int dividir(int...list) {
		int soma = 0;
		for(int d:list) {
			soma += d;
		}
		return soma;
	}

	public double dividir(double...list) {
		double soma = 0;
		for(double d:list) {
			soma += d;
		}
		return soma;
	}

	// SOMA
	public int soma(int...list) {
		int soma = 0;
		for(int d:list) {
			soma += d;
		}
		return soma;
	}

	public double soma(double...list) {
		double soma = 0;
		for(double d:list) {
			soma += d;
		}
		return soma;
	}

}
