package DeclaracaoVariaveis;

public class Ex1Variaveis {
	
	public static void main(String[] args) {
		
		long octal, binario, decimal, hexadecimal;
		//declaração de base octal
		octal = 02;
		//declaração de base binaria
		binario = 0b1000101;
		//declaração de base decimal (padrão)
		decimal = 100;
		//declaração de base hexadecimal
		hexadecimal = 0x32;
		
		System.out.println(octal);
		System.out.println(binario);
		System.out.println(decimal);
		System.out.println(hexadecimal);
		
	}

}
